"""Pricing of fixed-rate domestic tariffs.

Synthetic Forward Curve law: a customer's fixed unit rate is derived from a
synthetic forward price — generated upstream in `sim/forward_curve.py` from
real historical spot data — plus a margin. The forward price is itself the
synthetic, forward-looking number (Law 3); this module's only job is the
business-margin step on top of it.

This module is pure: it takes a forward price as a plain float and returns a
number. It does not import from `sim/` — the forward price arrives across the
`interface/` seam as a plain value, keeping the Point-in-Time Blindfold
structural and the curve-generation logic on the SIM side where Law 3 lives.

Generated by qwen2.5-coder:14b (local, via tools/delegate_ollama.py) and
reviewed/integrated by the frontier orchestrator — Phase 0b cross-model
delegation increment, revised in Phase 1c to take a pre-generated synthetic
forward price rather than computing its own spot-price lookback.
"""


def price_fixed_tariff(forward_price: float, margin: float = 0.05) -> float:
    """Price a fixed unit rate (£/MWh) for a customer from a forward price.

    forward_price is an already-generated SYNTHETIC forward price (£/MWh) —
    produced by `sim.forward_curve.generate_forward_price` from real
    historical spot data and passed in as a plain float across the
    `interface/` seam. This function applies a business margin on top of it
    (defaulting to 0.05, i.e. 5%) and returns the result.

    Returns the priced unit rate as a float (£/MWh).
    """
    return forward_price * (1 + margin)
